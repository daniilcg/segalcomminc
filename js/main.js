let currentLang = 'en';



// ===== LANGUAGE =====

function setLanguage(lang) {

    currentLang = lang;



    document.querySelectorAll('.lang-btn').forEach(btn => {

        btn.classList.toggle('active', btn.dataset.lang === lang);

    });



    document.querySelectorAll('[data-i18n]').forEach(el => {

        const keys = el.dataset.i18n.split('.');

        let value = translations[lang];

        for (let key of keys) {

            if (value && value[key] !== undefined) value = value[key];

            else { value = null; break; }

        }

        if (value !== null) el.innerHTML = value;

    });



    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {

        const keys = el.dataset.i18nPlaceholder.split('.');

        let value = translations[lang];

        for (let key of keys) {

            if (value && value[key] !== undefined) value = value[key];

            else { value = null; break; }

        }

        if (value !== null) el.placeholder = value;

    });



    localStorage.setItem('segal-lang', lang);

}



document.addEventListener('DOMContentLoaded', () => {

    const saved = localStorage.getItem('segal-lang') || 'en';

    setLanguage(saved);



    document.querySelectorAll('.lang-btn').forEach(btn => {

        btn.addEventListener('click', () => setLanguage(btn.dataset.lang));

    });

});



// ===== MOBILE MENU =====

document.addEventListener('DOMContentLoaded', () => {

    const toggle = document.getElementById('mobileToggle');

    const menu = document.getElementById('mobileMenu');



    if (toggle && menu) {

        toggle.addEventListener('click', () => {

            toggle.classList.toggle('active');

            menu.classList.toggle('open');

        });

        menu.querySelectorAll('a').forEach(link => {

            link.addEventListener('click', () => {

                toggle.classList.remove('active');

                menu.classList.remove('open');

            });

        });

    }

});



// ===== CURSOR =====

document.addEventListener('DOMContentLoaded', () => {
    const cursor = document.querySelector('.cursor');
    if (!cursor) return;

    document.addEventListener('mousemove', (e) => {
        cursor.style.left = e.clientX + 'px';
        cursor.style.top = e.clientY + 'px';
    });

    if ('ontouchstart' in window) {
        cursor.style.display = 'none';
        document.body.style.cursor = 'auto';
    }
});



// ===== SCROLL ANIMATIONS =====

document.addEventListener('DOMContentLoaded', () => {

    const elements = document.querySelectorAll('.service-card, .about-text, .about-window, .contact-info, .contact-form');



    const observer = new IntersectionObserver((entries) => {

        entries.forEach((entry) => {

            if (entry.isIntersecting) {

                entry.target.classList.add('visible');

            }

        });

    }, { threshold: 0.15, rootMargin: '0px 0px -50px 0px' });



    elements.forEach(el => {

        el.classList.add('fade-in');

        observer.observe(el);

    });

});



// ===== CONTACT FORM =====

document.addEventListener('DOMContentLoaded', () => {

    const form = document.getElementById('contactForm');

    if (form) {

        form.addEventListener('submit', (e) => {
            e.preventDefault();
            const name = document.getElementById('formName').value.trim();
            const email = document.getElementById('formEmail').value.trim();
            const serviceEl = document.getElementById('formService');
            const service = serviceEl?.value;
            const serviceLabel = serviceEl?.selectedOptions[0]?.textContent?.trim() || service;
            const message = document.getElementById('formMessage').value.trim();

            const errors = {
                ru: 'Заполните все поля и выберите услугу.',
                en: 'Please fill in all fields and select a service.',
                sr: 'Popunite sva polja i izaberite uslugu.'
            };

            if (!name || !email || !service || !message) {
                alert(errors[currentLang] || errors.en);
                return;
            }

            const subject = encodeURIComponent(`SEGAL COMMUNICATIONS — ${serviceLabel}`);
            const body = encodeURIComponent(`Name: ${name}\nEmail: ${email}\nService: ${serviceLabel}\n\n${message}`);
            window.location.href = `mailto:segalcomminc@gmail.com?subject=${subject}&body=${body}`;
        });

    }

});



// ===== SMOOTH SCROLL =====

document.addEventListener('DOMContentLoaded', () => {

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {

        anchor.addEventListener('click', function(e) {

            const href = this.getAttribute('href');

            if (href === '#') return;

            e.preventDefault();

            const target = document.querySelector(href);

            if (target) {

                target.scrollIntoView({ behavior: 'smooth', block: 'start' });

            }

        });

    });

});


